CREATE SEQUENCE Loc_Sequence
START WITH 1
INCREMENT BY 1;

CREATE TRIGGER Loc_Trigger
BEFORE INSERT ON Location
FOR EACH ROW
    BEGIN
        SELECT Loc_Sequence.NEXTVAL INTO :new.LOC_ID from dual;
    END;
RUN;

INSERT INTO Location (CITY, STATE, COUNTRY)
